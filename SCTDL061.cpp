using namespace std;

ll n, m;
ll dp[1005][1005];
ll cal(string a, string b)
{
    for (int i = 1; i <= n; i++)
        for (int j = 1; j <= m; j++)
        {
            if (a[i - 1] == b[j - 1])
                dp[i][j] = dp[i - 1][j - 1] + 1;
            else
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]);
        }
    return dp[n][m];
}

void solve()
{
    string a, b;
    cin >> a >> b;
    n = a.size();
    m = b.size();
    cout << cal(a, b) << endl;
    memset(dp, 0, sizeof(dp));
}

